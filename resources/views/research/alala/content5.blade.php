<h2 class="top">
Golongan Manusia di sekitar kita (Nadhom 27, 28, 29, 30)
</h2>

<h3>
Nadhom 27
</h3>
<pre style="padding-right: 32px;" class="arabic">فَمَا النَّاسُ اِلاَّ وَاحـِدٌ مِنْ ثَلاَثَةٍ ۞ شَرِيْفٌ وَمَشْرُوْفٌ وَمِثْلٌ مُقَاوِمُ
</pre>
ORA ONO MANUNGSO IKU WUJUD PERKORO   #   KEJOBO SIFAT SIJI SAKING TELUNG PERKORO
<br/>
SUWIJI SIFAT MULYA KAPINDHO DIMULYA'KE # KAPING TELU IYO MADANI KANCA-KANCANE
<br/>
Manusia (yang ada di sekitar kita) hanya salah satu dari tiga (golongan), yaitu orang yang mulia, rendah dan sepadan (dengan kita).
<hr/>

<h3>
Nadhom 28
</h3>
<pre class="arabic">فَاَمَّا الَّذِىْ فَوْقِىْ فَأَعْرِفُ قَــدْرَهُ ۞ وَاَتْبَــعُ فِيْهِ الْحَــقَّ وَالْحَــقُّ لاَزِمُ
</pre>
DENE WONG SAK DUWURKU AKU WERUH DRAJATE
LAN AKU MANUT HAKE MERGO HAK BARANG MESTI
<br/>
Orang yang mulia saya tahu derajatnya dan saya harus mengikuti sesuatu yang haq darinya,
<hr/>

<h3>
Nadhom 29
</h3>
<pre style="padding-right: 32px;" class="arabic">فَاَمَّا الَّذِىْ مِثْلِى فَاِنْ زَلَّ اَوْهَفَـــا ۞ تَفَضَّـلْتُ اِنَّ الْفَضْـلَ بِالْفَخْرِحَــــاكِمُ
</pre>
DENE WONG SAK PADAKU LAMUN WONG IKU KLIRU
PODO UGO IKU WONG KLUPUTAN MARANG AKU
MONGKO AKU AWEH KENUGRAHAN MARANG KANG SALAH
KRONO KENUGRAHAN NGUNGKULI SIFAT BUNGAH
<br/>
Dan orang yang sepadan denganku bila terpeleset atau salah padaku, maka aku lebih utama, karena keutamaan itu lebih dari kebahagiaan,
<hr/>

<h3>
Nadhom 30
</h3>
<pre class="arabic">فَاَمَّا الَّذِىْ دُوْنِىْ فَاَحْلَمُ دَائِبًـــــــا ۞ أَصُوْنُ بِهِ عِرْضِى وَاِنْ لاَمَ لاَئِمُ
</pre>
DENE WONG SAK NGISORKU AKU SABAR BIYOSO
NGAREKSO KAWIRANGAN NAJAN AKU DEN WODO
<br/>
Sedangkan orang yang rendah maka saya selalu memberikan kata maaf kepada mereka untuk menjaga kehormatanku walaupun banyak orang yang mencela.

<h2>
Jangan mendendam (nadhom  31)
</h2>

<h3>
Nadhom 31
</h3>
<pre class="arabic">دَعِ الْمَرْءَ لاَتُجْزِ عَلَى سُوْءِ فِعْلِهِ ۞ سَيَكْفِيْهِ مَا فِيْــــهِ وَمَا هُوَ فَاعِلُهُ
</pre>
TINGGALO SIRO ING WONG SIJI OLO LAKUNE
TEGESE OJO MALES OLO KANG DI LAKONI
<br/>
Jangan pedulikan orang lain (yang berbuat jahat kepadamu) jangan kau balas perbuatan jahatnya karena dia akan di balas oleh perbuatannya sendiri.

<h2>
Waktu yang bernilai (Nadhom 32)
</h2>
<h3>
Nadhom 32
</h3>
<pre class="arabic">أَلَيْسَتْ مِنَ الْخُسْرَانِ اَنَّ لَيَالِيَا ۞ تَمُرُّ بِلاَ نَفْعٍ وَتُحْسَبُ مِنْ عُمْرِىْ
</pre>
ONOTO KABEH DUDU GOLONGANE WONG TUNO
LIWATE KANTHI NGANGGUR DI ITUNG UMUR KITO
<br/>
Bukankah termasuk kerugian bila malam-malam berlalu tanpa aku manfaatkan sedangkan umurku terus berkurang?

